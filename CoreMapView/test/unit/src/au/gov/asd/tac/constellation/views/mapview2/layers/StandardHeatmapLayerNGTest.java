/**
 *
 * @author altair1673
 */
public class StandardHeatmapLayerNGTest {

    private static final Logger LOGGER = Logger.getLogger(StandardHeatmapLayerNGTest.class.getName());

    @BeforeClass
    public static void setUpClass() throws Exception {
        if (!FxToolkit.isFXApplicationThreadRunning()) {
            FxToolkit.registerPrimaryStage();
        }
    }

    @AfterClass
    public static void tearDownClass() throws Exception {
        try {
            FxToolkit.cleanupStages();
        } catch (TimeoutException ex) {
            LOGGER.log(Level.WARNING, "FxToolkit timed out trying to cleanup stages", ex);
        }
    }

    @BeforeMethod
    public void setUpMethod() throws Exception {
    }

    @AfterMethod
    public void tearDownMethod() throws Exception {
    }

    /**
     * Test of getWeight method, of class StandardHeatmapLayer.
     */
    @Test
    public void testGetWeight() {
        System.out.println("getWeight");

        final MapView mapView = Mockito.mock(MapView.class);

        final AbstractMarker marker = Mockito.spy(new PointMarker(mapView, -99, 0, (double) -1, (double) -2, ConstellationColor.WHITE));
        Mockito.when(marker.getConnectedNodeIdList()).thenCallRealMethod();
        Mockito.doCallRealMethod().when(marker).addNodeID(1);
        Mockito.when(marker.getWeight()).thenCallRealMethod();

        final StandardHeatmapLayer instance = new StandardHeatmapLayer(mapView, 0);
        final int expResult = 1;
        final int result = instance.getWeight(marker);
        assertEquals(result, expResult);

    }

}
