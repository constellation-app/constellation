    /**
     * Event handler for <code>txtFloat2</code> key releases.
     * <p>
     * This triggers float validation checks.
     *
     * @param evt The registered event.
     */
    private void txtFloat2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFloat2KeyReleased

        if (!validateFloat(txtFloat2.getText())) {
            txtFloat2.setForeground(Color.RED);
        } else {
            txtFloat2.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_txtFloat2KeyReleased

