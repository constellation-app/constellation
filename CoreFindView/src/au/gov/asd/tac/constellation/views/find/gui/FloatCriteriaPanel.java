    /**
     * Event handler for <code>txtFloat1</code> loss of focus.
     * <p>
     * This triggers float validation checks.
     *
     * @param evt The registered event.
     */
    private void txtFloat1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtFloat1FocusLost

        if (validateFloat(txtFloat1.getText())) {
            validFloat1 = Float.parseFloat(txtFloat1.getText());
        } else {
            txtFloat1.setText(Float.toString(validFloat1));
            txtFloat1.setForeground(Color.BLACK);
        }

        // Something may have changed, so save the state:
        parentPanel.saveStateToGraph();
    }//GEN-LAST:event_txtFloat1FocusLost

